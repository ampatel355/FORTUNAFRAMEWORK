"""Port paper.tex into the genuine MDPI (Risks) template: Definitions/mdpi.cls,
Chicago author-date (mdpi_chicago.bst), MDPI front/back-matter macros, BibTeX.

Reuses the existing body verbatim (faithfulness), transforming only:
  - theorem environments lowercase -> Capitalized (MDPI names),
  - \cite -> \citep (author-date),
and replaces the elsarticle front/back matter + manual bibliography with the
MDPI macros + \bibliography{fortuna}.
"""
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
L = (ROOT / "paper.tex").read_text().splitlines()


def blk(a, b):  # 1-indexed inclusive
    return "\n".join(L[a - 1:b])


# --- locate boundaries ---
i_abs0 = next(i for i, l in enumerate(L, 1) if "emergencystretch=3em" in l)
i_abs1 = next(i for i, l in enumerate(L, 1) if l.strip() == "\\endgroup")
i_intro = next(i for i, l in enumerate(L, 1) if l.strip() == "\\section{Introduction}")
i_appendix = next(i for i, l in enumerate(L, 1) if l.strip() == "\\appendix")
i_bm = next(i for i, l in enumerate(L, 1) if "\\noindent\\textbf{Supplementary Materials:}" in l)
# back matter is preceded by a \vspace{10pt}; cut appendix block before it
i_bm_start = i_bm
while "\\vspace" not in L[i_bm_start - 1]:
    i_bm_start -= 1

abstract = " ".join(blk(i_abs0 + 1, i_abs1 - 1).split())            # single paragraph
body = blk(i_intro, i_appendix - 1)                                 # sections 1-8
appendix = blk(i_appendix + 1, i_bm_start - 1)                      # appendix content (drop \appendix line)


def transform(s):
    for env in ("theorem", "lemma", "proposition", "definition", "assumption", "corollary", "remark"):
        Cap = env.capitalize()
        s = s.replace("\\begin{%s}" % env, "\\begin{%s}" % Cap)
        s = s.replace("\\end{%s}" % env, "\\end{%s}" % Cap)
    s = s.replace("\\cite{", "\\citep{")     # author-date parenthetical
    return s


body = transform(body)
appendix = transform(appendix)

