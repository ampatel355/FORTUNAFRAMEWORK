APPENDIX_WRAP = "\n\\appendixtitles{yes}\n\\appendixstart\n\\appendix\n"

TAIL = "\n\\reftitle{References}\n\\bibliography{fortuna}\n\n\\end{document}\n"

out = (PREAMBLE.replace("__ABSTRACT__", abstract) + "\n"
       + body + "\n"
       + BACKMATTER + "\n"
       + APPENDIX_WRAP + appendix + "\n"
       + TAIL)
(ROOT / "paper_mdpi.tex").write_text(out)
print("wrote paper_mdpi.tex (%d lines)" % len(out.splitlines()))
print("abstract words:", len(abstract.split()))
